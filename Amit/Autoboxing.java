// Autoboxing 

class Autoboxing{
	public static void main(String[] args)
	{
		int i =10;
		System.out.println(" i : "+i);
		Integer i1 = new Integer(i);
		System.out.println(" i1 : "+i1);

		char c = 'a';
		System.out.println(" c : "+c);
		Character c1 = new Character(c);
		System.out.println(" c1 : "+c1);


		
	}
}