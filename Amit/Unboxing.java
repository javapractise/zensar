// Unboxing

class Unboxing{
	public static void main(String[] args)
	{
		Integer i  = new Integer(100);
		System.out.println(" i : "+i);
		int i1 = i;
		System.out.println(" i1 : "+i1);

		Character c = new Character('a');
		System.out.println(" c : "+c);
		char c1 = c;
		System.out.println(" c1 : "+c1);
	}
}